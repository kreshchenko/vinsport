        <h2>Заказы</h2>
    Заказы принимаются ежедневно с  10.00 до 21.00. Если Вы сделали заказ через сайт в нерабочее время –
    мы с Вами свяжемся на следующий день.<br/>
    Для того, чтобы оформить заказ на сайте необходимо:
        <ol>
            <li>Выбрать товар/товары, которые Вам понравились, и добавить их в корзину.</li>
            <li>Перейти в корзину (правый верхний угол интернет-магазина) и проверить правильность выбранных товаров и их цены. </li>
            <li>Оформить заказ, введя необходимые данные в поля. </li>
            <li>В ближайшее время после заказа с Вами свяжется наш оператор.</li>
        </ol>

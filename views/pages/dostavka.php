    <p>
        <h2>Доставка</h2>
            <h3>по Виннице</h3>
    В городе Винница возможна доставка «Под двери» или самовывоз. Доставка по городу бесплатна при покупке товара на
    сумму выше 1000 грн. Доставка осуществляется на протяжении двух рабочих дней с момента заказа. При заказе товара на
    сумму меньше, чем 1000 грн, доставка составляет 50 грн.
    <h3>по Украине</h3>
    ППо Украине доставка осуществляется компанией НоваПошта. Вы сможете получить Ваш заказ в отделении компании
    (обязательно нужно указать номер склада). Оплата услуг транспортной компании осуществляется получателем при
    получении заказа. С нашей стороны доставка заказа в транспортную компанию бесплатна. После отправки товара
    мы уведомляем Вам номер транспортной декларации, по которому Вы сможете забрать заказ. Срок доставки по Украине
    обычно составляет 1-3 рабочих дней с момента отправки заказа. Отправка осуществляется с г. Винница. Узнать адрес
    представительства в Вашем городе и ознакомится с тарифами, отследить перемещение Вашего заказа Вы можете на сайте
    компании: <a href="http://novaposhta.ua/">Новая Почта</a>

    </p>
